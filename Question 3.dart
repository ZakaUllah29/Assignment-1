// Q.3: A student will not be allowed to sit in exam if his/her attendance.

void main() {
  int classesheld = 16;
  int classesattended = 10;
  num percentage = 10 / 16 * 100;
  if (percentage > 75) {
    print("No. Of Classes Held : $classesheld");
    print("No. Of Classes Attended : $classesattended");
    print("Percentage : $percentage%");
    print("You Can Sit in Exam");
  } else {
    print("No. Of Classes Held : $classesheld");
    print("No. Of Classes Attended : $classesattended");
    print("Percentage : $percentage%");
    print("You Can Not Sit in Exam");
  }
}
