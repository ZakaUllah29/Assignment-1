// Q:2 Take two variables and store age then using if/else condition

void main() {
// taking variables

  int zaka = 19;
  int moiz = 16;

  // determining oldest and youngest using if else

  if (zaka > moiz) {
    print("Oldest : Zaka($zaka)");
    print("Younger : Moiz($moiz)");
  } else {
    print("Oldest : Moiz");
    print("Younger : Zaka");
  }
}
